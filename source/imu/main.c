void sendData(void)
{
	for (IMU_Data_TypeDef i = 0; i < IMU_TYPE_MAX; i++)
	{
		if (daqSendImuData(&daq, i) != DAQ_OK)
		{
			taskDelete(0,0);
			taskDelete(0,0);
      taskDelete(0,0);
			taskCreate(errorHandler, 500);
			schedStart();
		}
	}
}

